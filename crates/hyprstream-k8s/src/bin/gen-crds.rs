// A developer CLI: printing progress to stdout is the point.
#![allow(clippy::print_stdout)]
//! Emit every hyprstream CRD as YAML into `crates/hyprstream-k8s/crds/` **and**
//! into the Helm chart's `charts/hyprstream/crds/` (Helm installs that
//! directory before any template; the files there cannot be templated).
//!
//! Run with `cargo run -p hyprstream-k8s --bin gen-crds`. The emitted files are
//! committed in both locations; CI (or a developer) re-runs this after changing
//! a schema, and the `committed_yaml_matches_generated` test fails if either
//! copy is stale. No cluster is required — the CRDs are derived purely from
//! the Rust types.

use std::error::Error;
use std::path::PathBuf;

fn main() -> Result<(), Box<dyn Error>> {
    // Write next to the crate source regardless of the current working dir.
    let manifest_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let out_dirs = [
        manifest_dir.join("crds"),
        // The chart ships the same manifests via its `crds/` directory.
        manifest_dir.join("../../charts/hyprstream/crds"),
    ];

    for (crd, stem) in hyprstream_k8s::crd_manifests() {
        let yaml = serde_yaml::to_string(&crd)?;
        let doc = format!("# Generated by `cargo run -p hyprstream-k8s --bin gen-crds`.\n# Do not edit by hand; edit the Rust types in crates/hyprstream-k8s/src/.\n{yaml}");
        for out_dir in &out_dirs {
            std::fs::create_dir_all(out_dir)?;
            let path = out_dir.join(format!("{stem}.yaml"));
            std::fs::write(&path, &doc)?;
            println!("wrote {}", path.display());
        }
    }
    Ok(())
}
