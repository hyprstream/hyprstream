/// Try to build a `ScopedClient` for a single request variant.
///
/// Returns `Ok(Some(client))` when the variant matches the scoped-client pattern:
/// the variant's type is a struct with a union, and a matching `{name}Result`
/// response variant exists with non-empty inner union variants.
///
/// Returns `Ok(None)` for any variant that doesn't match (primitive types,
/// structs without unions, missing response counterparts, etc.).
///
/// `require_scope_fields` — when `true`, skips structs whose non-union field
/// list is empty.  Used for nested client detection: a nested scope must have
/// at least one curried field to distinguish it from a plain method.
#[allow(clippy::too_many_arguments)]
fn build_scoped_client_for_variant(
    req_variant: &UnionVariant,
    response_variants: &[UnionVariant],
    all_structs: &[StructDef],
    nodes: &capnp::struct_list::Reader<capnp::schema_capnp::node::Owned>,
    node_map: &BTreeMap<u64, NodeInfo>,
    mcp_desc_id: Option<u64>,
    param_desc_id: Option<u64>,
    mcp_scope_id: Option<u64>,
    cli_hidden_id: Option<u64>,
    doc_example_id: Option<u64>,
    require_scope_fields: bool,
) -> Result<Option<ScopedClient>, String> {
    if is_primitive_capnp_type(&req_variant.type_name) {
        return Ok(None);
    }

    let inner_struct = match all_structs.iter().find(|s| s.name == req_variant.type_name) {
        Some(s) => s,
        None => return Ok(None),
    };

    if !inner_struct.has_union {
        return Ok(None);
    }

    // Nested scoped clients must have at least one non-union scope field.
    if require_scope_fields && inner_struct.non_union_fields().next().is_none() {
        return Ok(None);
    }

    let resp_variant_name = format!("{}Result", req_variant.name);
    let resp_variant = match response_variants
        .iter()
        .find(|v| v.name == resp_variant_name)
    {
        Some(v) => v,
        None => return Ok(None),
    };

    let inner_node_id = match find_struct_node_id(node_map, &req_variant.type_name) {
        Some(id) => id,
        None => return Ok(None),
    };
    let inner_node = nodes.get(node_map[&inner_node_id].index);
    let inner_req_variants = extract_union_variants(
        inner_node,
        nodes,
        node_map,
        mcp_desc_id,
        param_desc_id,
        mcp_scope_id,
        cli_hidden_id,
        doc_example_id,
        true, // scoped-client methods: scope mandatory (S3, #547)
    )?;

    let resp_node_id = match find_struct_node_id(node_map, &resp_variant.type_name) {
        Some(id) => id,
        None => return Ok(None),
    };
    let resp_node = nodes.get(node_map[&resp_node_id].index);
    let inner_resp_variants = extract_union_variants(
        resp_node,
        nodes,
        node_map,
        mcp_desc_id,
        param_desc_id,
        mcp_scope_id,
        cli_hidden_id,
        doc_example_id,
        false, // response variants carry no scope
    )?;

    if inner_req_variants.is_empty() || inner_resp_variants.is_empty() {
        return Ok(None);
    }

    let client_name = if req_variant.type_name.ends_with("Request") {
        format!(
            "{}Client",
            &req_variant.type_name[..req_variant.type_name.len() - 7]
        )
    } else {
        format!("{}Client", req_variant.type_name)
    };

    // Scope fields = non-union fields from the inner struct
    let scope_fields: Vec<FieldDef> = inner_struct.non_union_fields().cloned().collect();

    Ok(Some(ScopedClient {
        factory_name: req_variant.name.clone(),
        client_name,
        scope_fields,
        inner_request_variants: inner_req_variants,
        inner_response_variants: inner_resp_variants,
        capnp_inner_response: to_snake_case(&resp_variant.type_name),
        nested_clients: Vec::new(),
    }))
}

/// Detect scoped clients from the request/response pattern.
#[allow(clippy::too_many_arguments)]
fn detect_scoped_clients(
    request_variants: &[UnionVariant],
    response_variants: &[UnionVariant],
    all_structs: &[StructDef],
    nodes: &capnp::struct_list::Reader<capnp::schema_capnp::node::Owned>,
    node_map: &BTreeMap<u64, NodeInfo>,
    mcp_desc_id: Option<u64>,
    param_desc_id: Option<u64>,
    mcp_scope_id: Option<u64>,
    cli_hidden_id: Option<u64>,
    doc_example_id: Option<u64>,
) -> Result<Vec<ScopedClient>, String> {
    let mut scoped = Vec::new();

    for req_variant in request_variants {
        if let Some(sc) = build_scoped_client_for_variant(
            req_variant,
            response_variants,
            all_structs,
            nodes,
            node_map,
            mcp_desc_id,
            param_desc_id,
            mcp_scope_id,
            cli_hidden_id,
            doc_example_id,
            false, // top-level: don't require non-empty scope fields
        )? {
            scoped.push(sc);
        }
    }

    Ok(scoped)
}

/// Recursively detect nested scoped clients within a scoped client.
#[allow(clippy::too_many_arguments)]
fn detect_nested_scoped_clients_cgr(
    parent: &mut ScopedClient,
    all_structs: &[StructDef],
    nodes: &capnp::struct_list::Reader<capnp::schema_capnp::node::Owned>,
    node_map: &BTreeMap<u64, NodeInfo>,
    mcp_desc_id: Option<u64>,
    param_desc_id: Option<u64>,
    mcp_scope_id: Option<u64>,
    cli_hidden_id: Option<u64>,
    doc_example_id: Option<u64>,
) -> Result<(), String> {
    let mut nested = Vec::new();
    let mut nested_factory_names = Vec::new();

    for req_variant in &parent.inner_request_variants {
        if let Some(sc) = build_scoped_client_for_variant(
            req_variant,
            &parent.inner_response_variants,
            all_structs,
            nodes,
            node_map,
            mcp_desc_id,
            param_desc_id,
            mcp_scope_id,
            cli_hidden_id,
            doc_example_id,
            true, // nested: require at least one non-union scope field
        )? {
            nested_factory_names.push(req_variant.name.clone());
            nested.push(sc);
        }
    }

    // Filter out nested-scope variants from parent's inner variants
    if !nested_factory_names.is_empty() {
        parent
            .inner_request_variants
            .retain(|v| !nested_factory_names.contains(&v.name));
        parent.inner_response_variants.retain(|v| {
            let result_name = v.name.strip_suffix("Result").unwrap_or(&v.name);
            !nested_factory_names.contains(&result_name.to_owned())
        });
    }

    parent.nested_clients = nested;

    // Recurse into newly detected nested clients for deeper nesting
    for nested_client in &mut parent.nested_clients {
        detect_nested_scoped_clients_cgr(
            nested_client,
            all_structs,
            nodes,
            node_map,
            mcp_desc_id,
            param_desc_id,
            mcp_scope_id,
            cli_hidden_id,
            doc_example_id,
        )?;
    }

    Ok(())
}

/// Check if a type name is a primitive (not a struct reference).
///
/// Delegates to `is_primitive_capnp_type` in `schema::types` — the canonical
/// implementation. Kept for backward compatibility with existing callers.
pub fn is_primitive_type(type_name: &str) -> bool {
    is_primitive_capnp_type(type_name)
}

#[cfg(test)]
mod mandatory_scope_tests {
    use super::*;

    fn variant(name: &str, scope: &str, exempt: bool) -> UnionVariant {
        UnionVariant {
            name: name.to_owned(),
            type_name: "Void".to_owned(),
            description: String::new(),
            scope: scope.to_owned(),
            scope_exempt: exempt,
            cli_hidden: false,
            doc_example: String::new(),
        }
    }

    fn scoped(factory: &str, inner: Vec<UnionVariant>, nested: Vec<ScopedClient>) -> ScopedClient {
        ScopedClient {
            factory_name: factory.to_owned(),
            client_name: format!("{factory}Client"),
            scope_fields: vec![],
            inner_request_variants: inner,
            inner_response_variants: vec![],
            capnp_inner_response: String::new(),
            nested_clients: nested,
        }
    }

    #[test]
    fn scoped_method_passes() {
        let reqs = vec![variant("load", "write", false), variant("status", "query", false)];
        assert!(validate_mandatory_scope("model", &reqs, &[]).is_ok());
    }

    #[test]
    fn unscoped_leaf_is_build_error() {
        let reqs = vec![variant("load", "write", false), variant("oops", "", false)];
        let err = validate_mandatory_scope("model", &reqs, &[]).unwrap_err();
        assert!(err.contains("oops"), "{err}");
        assert!(err.contains("mandatory"), "{err}");
    }

    #[test]
    fn scope_exempt_satisfies_requirement() {
        let reqs = vec![variant("check", "", true)];
        assert!(validate_mandatory_scope("policy", &reqs, &[]).is_ok());
    }

    #[test]
    fn dispatcher_variant_is_exempt_but_its_leaves_are_checked() {
        // Top-level `ttt` is a dispatcher (matches scoped_clients factory_name) and
        // carries no scope — that's fine. Its leaves must each carry a scope.
        let reqs = vec![variant("ttt", "", false)];
        let good = scoped("ttt", vec![variant("train", "train", false)], vec![]);
        assert!(validate_mandatory_scope("model", &reqs, &[good]).is_ok());

        let bad = scoped("ttt", vec![variant("train", "", false)], vec![]);
        let err = validate_mandatory_scope("model", &reqs, &[bad]).unwrap_err();
        assert!(err.contains("ttt train"), "{err}");
    }

    #[test]
    fn nested_dispatcher_leaves_are_checked() {
        // worktree -> ctl (nested dispatcher); ctl's leaf must carry a scope.
        let reqs = vec![variant("worktree", "", false)];
        let ctl_bad = scoped("ctl", vec![variant("log", "", false)], vec![]);
        let worktree = scoped(
            "worktree",
            vec![variant("read", "query", false), variant("ctl", "", false)],
            vec![ctl_bad],
        );
        let err = validate_mandatory_scope("registry", &reqs, &[worktree]).unwrap_err();
        assert!(err.contains("log"), "{err}");
    }
}
