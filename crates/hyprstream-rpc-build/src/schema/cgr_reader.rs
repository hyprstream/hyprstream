/// A resolved node from the CGR node graph.
struct NodeInfo {
    id: u64,
    display_name: String,
    /// Short name extracted from display_name (after last ':')
    short_name: String,
    /// Index into the CGR nodes list (for re-reading)
    index: u32,
    /// Parent node ID (scope_id)
    #[allow(dead_code)]
    parent_id: u64,
}

/// Parse the CodeGeneratorRequest into a ParsedSchema.
fn parse_cgr(
    cgr: capnp::schema_capnp::code_generator_request::Reader,
    service_name: &str,
) -> Result<ParsedSchema, String> {
    let nodes = cgr.get_nodes().map_err(|e| format!("{e}"))?;

    // Build node map: id -> NodeInfo (BTreeMap for deterministic iteration)
    let mut node_map: BTreeMap<u64, NodeInfo> = BTreeMap::new();
    for i in 0..nodes.len() {
        let node = nodes.get(i);
        let display_name = node
            .get_display_name()
            .map_err(|e| format!("{e}"))?
            .to_str()
            .map_err(|e| format!("{e}"))?
            .to_owned();
        let short_name = display_name
            .rsplit(':')
            .next()
            .unwrap_or(&display_name)
            .to_owned();
        node_map.insert(
            node.get_id(),
            NodeInfo {
                id: node.get_id(),
                display_name,
                short_name,
                index: i,
                parent_id: node.get_scope_id(),
            },
        );
    }

    // Find annotation node IDs by display_name
    let mcp_desc_id = find_annotation_id(&nodes, &node_map, "mcpDescription");
    let param_desc_id = find_annotation_id(&nodes, &node_map, "paramDescription");
    // S3 (#547): the mandatory scope annotation is `$scope` (alias `$capability`).
    // Either annotation id (whichever the schema imports) feeds the same extraction.
    let mcp_scope_id = find_annotation_id(&nodes, &node_map, "scope")
        .or_else(|| find_annotation_id(&nodes, &node_map, "capability"));
    let cli_hidden_id = find_annotation_id(&nodes, &node_map, "cliHidden");
    let domain_type_id = find_annotation_id(&nodes, &node_map, "domainType");
    let fixed_size_id = find_annotation_id(&nodes, &node_map, "fixedSize");
    let optional_id = find_annotation_id(&nodes, &node_map, "optional");
    let serde_rename_id = find_annotation_id(&nodes, &node_map, "serdeRename");
    let doc_example_id = find_annotation_id(&nodes, &node_map, "docExample");

    let pascal = to_pascal_case(service_name);
    let request_name = format!("{pascal}Request");
    let response_name = format!("{pascal}Response");

    // Extract all structs (for type resolution in codegen)
    let all_structs = extract_all_structs(
        &nodes,
        &node_map,
        service_name,
        mcp_desc_id,
        param_desc_id,
        domain_type_id,
        fixed_size_id,
        optional_id,
        serde_rename_id,
    )?;

    // Extract all enums (pass all_structs for imported type resolution)
    let enums = extract_all_enums(&nodes, &node_map, service_name, &all_structs)?;

    // Find request and response struct node IDs — data-only schemas may not have them
    let request_node_id = find_struct_node_id(&node_map, &request_name);
    let response_node_id = find_struct_node_id(&node_map, &response_name);

    let (request_variants, response_variants) = match (request_node_id, response_node_id) {
        (Some(req_id), Some(resp_id)) => {
            let request_node = nodes.get(node_map[&req_id].index);
            let response_node = nodes.get(node_map[&resp_id].index);

            let rv = extract_union_variants(
                request_node,
                &nodes,
                &node_map,
                mcp_desc_id,
                param_desc_id,
                mcp_scope_id,
                cli_hidden_id,
                doc_example_id,
                true, // request methods: scope mandatory (S3, #547)
            )?;
            let rs = extract_union_variants(
                response_node,
                &nodes,
                &node_map,
                mcp_desc_id,
                param_desc_id,
                mcp_scope_id,
                cli_hidden_id,
                doc_example_id,
                false, // response variants carry no scope
            )?;

            if rv.is_empty() || rs.is_empty() {
                (vec![], vec![])
            } else {
                (rv, rs)
            }
        }
        _ => (vec![], vec![]),
    };

    let is_data_only = request_variants.is_empty();

    // Detect scoped clients (nested union pattern) — skip for data-only schemas
    let scoped_clients = if is_data_only {
        Vec::new()
    } else {
        let mut sc = detect_scoped_clients(
            &request_variants,
            &response_variants,
            &all_structs,
            &nodes,
            &node_map,
            mcp_desc_id,
            param_desc_id,
            mcp_scope_id,
            cli_hidden_id,
            doc_example_id,
        )?;

        // Recursively detect nested scoped clients (3rd level)
        for s in &mut sc {
            detect_nested_scoped_clients_cgr(
                s,
                &all_structs,
                &nodes,
                &node_map,
                mcp_desc_id,
                param_desc_id,
                mcp_scope_id,
                cli_hidden_id,
                doc_example_id,
            )?;
        }
        sc
    };

    // Partition: for data-only schemas keep all structs; otherwise separate request/response
    let (referenced, request_struct, response_struct) = if is_data_only {
        (all_structs, None, None)
    } else {
        let mut req = None;
        let mut resp = None;
        let mut refs = Vec::new();
        for s in all_structs {
            if s.name == request_name {
                req = Some(s);
            } else if s.name == response_name {
                resp = Some(s);
            } else {
                refs.push(s);
            }
        }
        (refs, req, resp)
    };

    // S3 (#547): scope is MANDATORY on every leaf method. Validate after scoped-
    // client detection so dispatcher variants (which carry no scope of their own;
    // their leaves do) are correctly exempt.
    validate_mandatory_scope(service_name, &request_variants, &scoped_clients)?;

    Ok(ParsedSchema {
        request_variants,
        response_variants,
        structs: referenced,
        scoped_clients,
        enums,
        request_struct,
        response_struct,
    })
}

/// S3 (#547): enforce that every leaf RPC method declares a `$scope`/`$capability`.
///
/// A method with no scope is a BUILD ERROR — there are no silently-public methods.
/// Dispatcher variants (whose payload is a nested scoped `*Request` union) are exempt:
/// they route into a sub-client whose own leaf methods carry the scope. Such a
/// dispatcher is recognized by its variant name matching a detected scoped client's
/// `factory_name`; its leaves are validated recursively.
fn validate_mandatory_scope(
    service_name: &str,
    request_variants: &[UnionVariant],
    scoped_clients: &[ScopedClient],
) -> Result<(), String> {
    fn err(service_name: &str, path: &str, name: &str) -> String {
        format!(
            "service `{service_name}`: method `{path}{name}` has no `$scope`/`$capability` annotation \
             — scope is mandatory (S3, epic #547). Add e.g. `$scope(query)` for a read-only \
             (side-effect-free) method or `$scope(write)` for a mutating one; or, only if the method \
             genuinely cannot require authorization, declare `$scopeExempt(\"<reason>\")`."
        )
    }

    // Recurse into a scoped client's leaves + nested dispatchers.
    fn check_scoped(service_name: &str, path: &str, sc: &ScopedClient) -> Result<(), String> {
        let here = format!("{path}{} ", sc.factory_name);
        let nested_names: Vec<&str> =
            sc.nested_clients.iter().map(|n| n.factory_name.as_str()).collect();
        for v in &sc.inner_request_variants {
            // A nested dispatcher inside this scope is itself exempt; recurse instead.
            if nested_names.contains(&v.name.as_str()) {
                continue;
            }
            if v.scope.is_empty() && !v.scope_exempt {
                return Err(err(service_name, &here, &v.name));
            }
        }
        for n in &sc.nested_clients {
            check_scoped(service_name, &here, n)?;
        }
        Ok(())
    }

    let dispatcher_names: Vec<&str> =
        scoped_clients.iter().map(|sc| sc.factory_name.as_str()).collect();
    for v in request_variants {
        if dispatcher_names.contains(&v.name.as_str()) {
            continue; // dispatcher: leaves validated via check_scoped below
        }
        if v.scope.is_empty() && !v.scope_exempt {
            return Err(err(service_name, "", &v.name));
        }
    }
    for sc in scoped_clients {
        check_scoped(service_name, "", sc)?;
    }
    Ok(())
}

