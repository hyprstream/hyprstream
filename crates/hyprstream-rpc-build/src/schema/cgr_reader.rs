/// Extract a single struct definition from a CGR node, including wire format info.
#[allow(clippy::too_many_arguments)]
fn extract_struct_from_node(
    node: capnp::schema_capnp::node::Reader,
    info: &NodeInfo,
    nodes: &capnp::struct_list::Reader<capnp::schema_capnp::node::Owned>,
    node_map: &BTreeMap<u64, NodeInfo>,
    mcp_desc_id: Option<u64>,
    param_desc_id: Option<u64>,
    domain_type_id: Option<u64>,
    fixed_size_id: Option<u64>,
    optional_id: Option<u64>,
    serde_rename_id: Option<u64>,
    origin_file: Option<String>,
) -> Result<Option<StructDef>, String> {
    let struct_reader = match node.which() {
        Ok(capnp::schema_capnp::node::Struct(s)) => s,
        _ => return Ok(None),
    };

    // Synthetic group nodes (capnp emits one per `:group { ... }` arm, named
    // `Parent.armName`) are NOT standalone types in any language binding — their
    // leaf fields are inlined into the parent. Their dotted name is also not a
    // valid Rust/TS identifier. Skip them: their structured shape is captured in
    // the parent union's `union_arms`.
    if struct_reader.get_is_group() {
        return Ok(None);
    }

    let domain_type = {
        let annotations = node.get_annotations().map_err(|e| format!("{e}"))?;
        let dt = extract_annotation_text(annotations, domain_type_id);
        if dt.is_empty() {
            None
        } else {
            Some(dt)
        }
    };

    // Wire format info from CGR
    let data_words = struct_reader.get_data_word_count();
    let pointer_words = struct_reader.get_pointer_count();
    let discriminant_count = struct_reader.get_discriminant_count();
    let discriminant_offset = struct_reader.get_discriminant_offset();

    let fields_reader = struct_reader.get_fields().map_err(|e| format!("{e}"))?;
    let mut fields = Vec::new();
    let mut has_union = false;

    let ann = FieldAnnotationIds {
        mcp_desc_id,
        param_desc_id,
        fixed_size_id,
        optional_id,
        serde_rename_id,
    };

    for j in 0..fields_reader.len() {
        let field = fields_reader.get(j);
        let disc = field.get_discriminant_value();

        if disc != 0xFFFF {
            has_union = true;
        }

        // Slot fields go through the shared `field_def_from_slot` helper (also used
        // by the union-arm group-leaf loop) so the two can't drift. Group fields
        // keep the flat `("Group", 0, Group)` fallback so the TS/wire-format path is
        // byte-for-byte unchanged — their structured shape lives in `union_arms`.
        let field_def = match field.which() {
            Ok(capnp::schema_capnp::field::Slot(slot)) => {
                field_def_from_slot(field, slot, node_map, ann)?
            }
            Ok(capnp::schema_capnp::field::Group(_)) => {
                let field_name = field
                    .get_name()
                    .map_err(|e| format!("{e}"))?
                    .to_str()
                    .map_err(|e| format!("{e}"))?
                    .to_owned();
                let description = {
                    let annotations = field.get_annotations().map_err(|e| format!("{e}"))?;
                    let desc = extract_annotation_text(annotations, param_desc_id);
                    if desc.is_empty() {
                        extract_annotation_text(
                            field.get_annotations().map_err(|e| format!("{e}"))?,
                            mcp_desc_id,
                        )
                    } else {
                        desc
                    }
                };
                let fixed_size = extract_annotation_u32(
                    field.get_annotations().map_err(|e| format!("{e}"))?,
                    fixed_size_id,
                );
                let optional = has_annotation(
                    field.get_annotations().map_err(|e| format!("{e}"))?,
                    optional_id,
                );
                let serde_rename = {
                    let sr = extract_annotation_text(
                        field.get_annotations().map_err(|e| format!("{e}"))?,
                        serde_rename_id,
                    );
                    if sr.is_empty() {
                        None
                    } else {
                        Some(sr)
                    }
                };
                FieldDef {
                    name: field_name,
                    type_name: "Group".into(),
                    description,
                    fixed_size,
                    optional,
                    slot_offset: 0,
                    section: FieldSection::Group,
                    discriminant_value: disc,
                    serde_rename,
                }
            }
            Err(e) => return Err(format!("Field error: {e}")),
        };

        // We include ALL fields (including union members) for wire-format completeness.
        fields.push(field_def);
    }

    if struct_reader.get_discriminant_count() > 0 {
        has_union = true;
    }

    // Structured union-arm view (Void / single-type / group) for Rust enum codegen.
    // Only populated for unions; non-union structs leave this empty so the flat
    // `fields` path is byte-for-byte unchanged.
    let union_arms = if discriminant_count > 0 {
        extract_union_arms(
            struct_reader,
            nodes,
            node_map,
            FieldAnnotationIds {
                mcp_desc_id,
                param_desc_id,
                fixed_size_id,
                optional_id,
                serde_rename_id,
            },
        )?
    } else {
        Vec::new()
    };

    Ok(Some(StructDef {
        name: info.short_name.clone(),
        fields,
        has_union,
        domain_type,
        origin_file,
        data_words,
        pointer_words,
        discriminant_count,
        discriminant_offset,
        union_arms,
    }))
}

/// Extract all struct definitions from the schema (for the given service file).
///
/// Two-pass extraction:
/// 1. First pass: extract local types from this schema file
/// 2. Second pass: find imported types referenced by variants/fields but not in the local set
#[allow(clippy::too_many_arguments)]
fn extract_all_structs(
    nodes: &capnp::struct_list::Reader<capnp::schema_capnp::node::Owned>,
    node_map: &BTreeMap<u64, NodeInfo>,
    service_name: &str,
    mcp_desc_id: Option<u64>,
    param_desc_id: Option<u64>,
    domain_type_id: Option<u64>,
    fixed_size_id: Option<u64>,
    optional_id: Option<u64>,
    serde_rename_id: Option<u64>,
) -> Result<Vec<StructDef>, String> {
    let file_prefix = format!("{service_name}.capnp:");
    let mut structs = Vec::new();

    // Pass 1: extract local types
    for info in node_map.values() {
        if !info.display_name.starts_with(&file_prefix) {
            continue;
        }

        let node = nodes.get(info.index);
        if let Some(s) = extract_struct_from_node(
            node,
            info,
            nodes,
            node_map,
            mcp_desc_id,
            param_desc_id,
            domain_type_id,
            fixed_size_id,
            optional_id,
            serde_rename_id,
            None,
        )? {
            structs.push(s);
        }
    }

    // Pass 2 (iterative): find imported structs transitively referenced by any
    // already-known struct (local or previously discovered imported).  We loop
    // until no new types are added — typically 2–3 rounds for nested imports
    // (e.g. ChatMessage → ToolCall → ToolCallFunction).
    let mut known_names: std::collections::HashSet<String> =
        structs.iter().map(|s| s.name.clone()).collect();

    loop {
        let mut referenced_names: std::collections::HashSet<String> =
            std::collections::HashSet::new();

        // Collect refs from fields of all known structs (local + imported).
        for s in &structs {
            for f in &s.fields {
                collect_type_refs(&f.type_name, &mut referenced_names);
            }
            // Group-arm leaf types are not in the flat `fields` list, so feed them
            // (and single-type arm payloads, for robustness) into the ref collector.
            for arm in &s.union_arms {
                match &arm.payload {
                    ArmPayload::Type(name) => collect_type_refs(name, &mut referenced_names),
                    ArmPayload::Group(leaves) => {
                        for leaf in leaves {
                            collect_type_refs(&leaf.type_name, &mut referenced_names);
                        }
                    }
                    ArmPayload::Void => {}
                }
            }
        }

        // Also collect from union variant fields of local CGR nodes.
        for info in node_map.values() {
            if !info.display_name.starts_with(&file_prefix) {
                continue;
            }
            let node = nodes.get(info.index);
            if let Ok(capnp::schema_capnp::node::Struct(s)) = node.which() {
                if let Ok(fields) = s.get_fields() {
                    for i in 0..fields.len() {
                        let field = fields.get(i);
                        if field.get_discriminant_value() != 0xFFFF {
                            if let Ok(capnp::schema_capnp::field::Slot(slot)) = field.which() {
                                if let Ok(type_reader) = slot.get_type() {
                                    let type_name = resolve_type_name(type_reader, node_map);
                                    collect_type_refs(&type_name, &mut referenced_names);
                                }
                            }
                        }
                    }
                }
            }
        }

        // Add any referenced types not yet in the known set.
        let mut added = false;
        for ref_name in &referenced_names {
            if known_names.contains(ref_name) || is_primitive_type(ref_name) {
                continue;
            }
            if let Some(info) = node_map.values().find(|n| n.short_name == *ref_name) {
                if !info.display_name.starts_with(&file_prefix) {
                    let origin = extract_file_stem(&info.display_name);
                    let node = nodes.get(info.index);
                    if let Some(s) = extract_struct_from_node(
                        node,
                        info,
                        nodes,
                        node_map,
                        mcp_desc_id,
                        param_desc_id,
                        domain_type_id,
                        fixed_size_id,
                        optional_id,
                        serde_rename_id,
                        Some(origin),
                    )? {
                        known_names.insert(ref_name.clone());
                        structs.push(s);
                        added = true;
                    }
                } else {
                    // Type is local — just mark it known so we don't revisit.
                    known_names.insert(ref_name.clone());
                }
            }
        }

        if !added {
            break;
        }
    }

    Ok(structs)
}

/// Extract file stem from a CGR display_name (e.g., "streaming.capnp:StreamInfo" -> "streaming").
fn extract_file_stem(display_name: &str) -> String {
    display_name
        .split(':')
        .next()
        .unwrap_or("")
        .strip_suffix(".capnp")
        .unwrap_or("")
        .to_owned()
}

/// Collect struct/enum type references from a capnp type name string.
fn collect_type_refs(type_name: &str, refs: &mut std::collections::HashSet<String>) {
    if let Some(inner) = type_name
        .strip_prefix("List(")
        .and_then(|s| s.strip_suffix(')'))
    {
        collect_type_refs(inner, refs);
    } else if !is_primitive_type(type_name) {
        refs.insert(type_name.to_owned());
    }
}

