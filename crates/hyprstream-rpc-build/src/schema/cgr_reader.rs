//! CGR-based Cap'n Proto schema parser.
//!
//! Reads binary CodeGeneratorRequest (CGR) files produced by `capnp compile`
//! via build.rs, and produces `ParsedSchema` with full wire format info.
//!
//! Advantages over text parsing:
//! - Full type resolution (struct names resolved via node graph)
//! - Native annotation extraction (no text stripping hacks)
//! - Deterministic output (BTreeMap for node ordering)
//! - Wire format offsets (data_words, pointer_words, slot_offset) for codegen
//! - Discriminant values available directly

use std::collections::BTreeMap;
use std::path::Path;

use super::types::*;
use crate::util::{to_pascal_case, to_snake_case};

/// Parse a schema from a CGR binary file at a given path.
pub fn parse_from_cgr_path(cgr_path: &Path, name: &str) -> Result<ParsedSchema, String> {
    let file = std::fs::File::open(cgr_path)
        .map_err(|e| format!("Failed to open {}: {e}", cgr_path.display()))?;

    let reader = capnp::serialize::read_message(
        std::io::BufReader::new(file),
        capnp::message::ReaderOptions::new(),
    )
    .map_err(|e| format!("Failed to read CGR for '{name}': {e}"))?;

    let cgr = reader
        .get_root::<capnp::schema_capnp::code_generator_request::Reader>()
        .map_err(|e| format!("Failed to parse CGR root for '{name}': {e}"))?;

    parse_cgr(cgr, name)
}

/// Parse a schema from a CGR binary file in OUT_DIR (for proc-macro use).
///
/// The CGR file is generated by build.rs via `capnpc::CompilerCommand::raw_code_generator_request_path()`.
pub fn parse_from_cgr(name: &str) -> Result<ParsedSchema, String> {
    let out_dir = std::env::var("OUT_DIR")
        .map_err(|_| "OUT_DIR not set — CGR files require build.rs to run first".to_owned())?;
    let cgr_path = Path::new(&out_dir).join(format!("{name}.cgr"));

    if cgr_path.exists() {
        return parse_from_cgr_path(&cgr_path, name);
    }

    // Fall back to dependency crate OUT_DIRs.
    // Cargo exposes DEP_<CRATE>_<KEY> from dependency build scripts.
    // hyprstream-rpc-std exports its OUT_DIR via `cargo:out_dir=...`
    for env_key in [
        "DEP_HYPRSTREAM_RPC_STD_OUT_DIR",
        "DEP_HYPRSTREAM_RPC_OUT_DIR",
    ] {
        if let Ok(dep_out_dir) = std::env::var(env_key) {
            let dep_cgr = Path::new(&dep_out_dir).join(format!("{name}.cgr"));
            if dep_cgr.exists() {
                return parse_from_cgr_path(&dep_cgr, name);
            }
        }
    }

    parse_from_cgr_path(&cgr_path, name) // will produce the original error
}

