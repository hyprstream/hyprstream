/// Return the default TS expression for a Cap'n Proto type (used for optional field coalescing).
fn default_value_expr(type_name: &str) -> &str {
    match type_name {
        "Bool" => "false",
        "UInt8" | "UInt16" | "UInt32" | "Int8" | "Int16" | "Int32" | "Float32" | "Float64" => "0",
        "UInt64" | "Int64" => "0n",
        "Text" => "''",
        "Data" => "new Uint8Array(0)",
        _ if type_name.starts_with("List(") => "[]",
        _ => "undefined",
    }
}

/// Return the TypeScript getter method name on `CapnpReader` / `StructReader`.
fn getter_method(type_name: &str) -> &str {
    match type_name {
        "Bool" => "getBool",
        "UInt8" => "getUint8",
        "UInt16" => "getUint16",
        "UInt32" => "getUint32",
        "UInt64" => "getUint64",
        "Int8" => "getInt8",
        "Int16" => "getInt16",
        "Int32" => "getInt32",
        "Int64" => "getInt64",
        "Float32" => "getFloat32",
        "Float64" => "getFloat64",
        "Text" => "getText",
        "Data" => "getData",
        // Callers only ever reach this function for `is_data_scalar`-narrowed
        // types or a literal Text/Data check — List(_) always resolves through
        // `list_getter_method` at the call site instead. Anything else getting
        // here is a generator bug: fail loudly rather than silently guessing
        // `getText` (the old behavior masked exactly this class of bug — #725).
        other => panic!(
            "ts_codegen: getter_method called with unsupported type `{other}` — this \
             indicates a generator bug in the call site (missing a scalar/Text/Data \
             guard), never silently degrade to a guessed getter (#725)."
        ),
    }
}

/// Check if a capnp type is a simple scalar (data section, not pointer).
pub(crate) fn is_data_scalar(type_name: &str) -> bool {
    matches!(
        type_name,
        "Bool"
            | "UInt8"
            | "UInt16"
            | "UInt32"
            | "UInt64"
            | "Int8"
            | "Int16"
            | "Int32"
            | "Int64"
            | "Float32"
            | "Float64"
    )
}

/// Extract the inner type name from a `List(Foo)` type string. Returns `None` for non-list types.
fn extract_list_inner_type(type_name: &str) -> Option<&str> {
    if type_name.starts_with("List(") && type_name.ends_with(')') {
        Some(&type_name[5..type_name.len() - 1])
    } else {
        None
    }
}

/// Check if a capnp type name is a primitive (not a struct reference).
///
/// Delegates to `is_primitive_capnp_type` in `schema::types` — the canonical
/// implementation shared with the CGR reader.
pub(crate) fn is_primitive(type_name: &str) -> bool {
    hyprstream_rpc_build::schema::types::is_primitive_capnp_type(type_name)
}

/// Map a union-member field to the TypeScript type carried in the `data` slot.
///
/// Mirrors the per-variant `data` expression produced by the parser emitters:
/// - `Void`              → `undefined`
/// - scalars / `Bool`    → the TS scalar (`number` / `bigint` / `boolean`)
/// - `Text`              → `string`
/// - `Data`              → `Uint8Array`
/// - `List(Text)`        → `string[]`
/// - `List(Struct)`      → `Inner[]` (elements are parsed into objects)
/// - `Option*` wrapper   → `T | undefined` (absence is `undefined`, never `null`)
/// - struct              → `TypeName | null` (the parser returns `null` on a null pointer)
///
/// Keeping this aligned with the parser guarantees the typed discriminated union
/// is sound against the runtime values callers actually receive. Shared by both
/// the interface emitter (`interfaces.rs`) and the parser-result type aliases
/// (`parsers.rs`).
pub(crate) fn union_variant_data_type(f: &FieldDef) -> String {
    let tn = f.type_name.as_str();
    if tn == "Void" {
        return "undefined".to_owned();
    }
    if tn.starts_with("List(") {
        // capnp_to_ts_type already maps List(T) → T[].
        return capnp_to_ts_type(tn);
    }
    // `is_primitive` already covers Text/Data (and Void/List); no extra checks.
    if is_primitive(tn) {
        return capnp_to_ts_type(tn);
    }
    // Option* wrapper structs encode absence as `undefined`
    // (capnp_to_ts_type maps `OptionUint32` → `number | undefined`); they live in
    // the pointer section but must NOT get the struct-pointer `| null` suffix.
    if tn.starts_with("Option") {
        return capnp_to_ts_type(tn);
    }
    // Struct (or enum) reference. Struct pointers can be null at runtime; enums
    // are data-section and never null, but the only data-section non-scalar union
    // payloads are enums, which `capnp_to_ts_type` maps to their type name.
    if matches!(f.section, FieldSection::Pointer) {
        format!("{} | null", capnp_to_ts_type(tn))
    } else {
        capnp_to_ts_type(tn)
    }
}

/// If union field `uf` of struct `sd` is an inline `group` arm, return its leaf
/// fields (from the structured `union_arms` view). `None` for Void/single-type
/// arms.
///
/// A capnp `group` arm carries an inlined anonymous struct whose leaf fields are
/// allocated their own slots in the ENCLOSING struct's data/pointer sections;
/// each leaf `FieldDef` therefore already carries a correct `slot_offset` +
/// `section` (extracted by the same `field_def_from_slot` path as a normal
/// field), so build/parse can read/write them directly on the enclosing
/// reader/builder.
pub(crate) fn group_arm_leaves<'a>(sd: &'a StructDef, uf: &FieldDef) -> Option<&'a [FieldDef]> {
    if uf.section != FieldSection::Group {
        return None;
    }
    sd.union_arms
        .iter()
        .find(|a| a.name == uf.name)
        .and_then(|a| match &a.payload {
            ArmPayload::Group(leaves) => Some(leaves.as_slice()),
            _ => None,
        })
}

/// Name of the synthesized TypeScript interface for a group arm, e.g. the
/// `dropOldest` arm of `OverflowPolicy` → `OverflowPolicyDropOldest`.
pub(crate) fn group_arm_type_name(struct_name: &str, arm_name: &str) -> String {
    format!("{struct_name}{}", to_pascal_case(arm_name))
}

/// The `data:` TypeScript type for one arm of struct `sd`'s discriminated union.
///
/// Same as [`union_variant_data_type`] for Void/scalar/struct arms, but a group
/// arm resolves to its synthesized interface (`{Struct}{Arm}` | null) instead of
/// the bare, undefined `Group`. Shared by the interface emitter and the parser
/// result-type aliases so type/build/parse agree.
pub(crate) fn union_arm_data_type(sd: &StructDef, f: &FieldDef) -> String {
    if group_arm_leaves(sd, f).is_some() {
        format!("{} | null", group_arm_type_name(&sd.name, &f.name))
    } else {
        union_variant_data_type(f)
    }
}

/// Build a TypeScript array literal from an enum's variants (camelCase names as strings).
///
/// Example output: `['active', 'inactive', 'pending']`
fn enum_ordinal_array(ed: &EnumDef) -> String {
    let variants: Vec<String> = ed
        .variants
        .iter()
        .map(|(name, _)| format!("'{}'", to_camel_case(name)))
        .collect();
    format!("[{}]", variants.join(", "))
}

/// Emit a `setUint16` call that converts a TypeScript string enum to its wire ordinal.
///
/// Generates: `{indent}{builder_var}.setUint16({byte_off}, [...].indexOf({value_expr}));`
pub(crate) fn emit_enum_setter(
    out: &mut String,
    indent: &str,
    builder_var: &str,
    byte_off: u32,
    value_expr: &str,
    ed: &EnumDef,
) {
    out.push_str(&format!(
        "{indent}{builder_var}.setUint16({byte_off}, {}.indexOf({value_expr}));\n",
        enum_ordinal_array(ed)
    ));
}

/// Return a TypeScript expression that reads a UInt16 ordinal and maps it to a string enum.
///
/// Example output: `(['active', 'inactive'][reader.getUint16(4)] ?? 'unknown') as Status`
pub(crate) fn emit_enum_getter_expr(
    reader_var: &str,
    byte_off: u32,
    type_name: &str,
    ed: &EnumDef,
) -> String {
    format!(
        "({}[{reader_var}.getUint16({byte_off})] ?? 'unknown') as {type_name}",
        enum_ordinal_array(ed)
    )
}

