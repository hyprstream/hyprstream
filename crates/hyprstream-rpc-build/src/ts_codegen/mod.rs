//! TypeScript code generation from parsed Cap'n Proto schemas.
//!
//! Generates:
//! - `capnp.ts`         — Wire format runtime (CapnpMessageBuilder + CapnpReader)
//! - `{service}.ts`     — Per-service interfaces, builders, parsers, clients
//! - `index.ts`         — Re-exports

mod message;
mod interfaces;
mod builders;
mod parsers;
mod client;

use std::path::Path;

use hyprstream_rpc_build::schema::types::{FieldDef, FieldSection, ParsedSchema};
use hyprstream_rpc_build::util::to_pascal_case;

/// Generate all TypeScript files from parsed schemas.
#[allow(clippy::print_stdout)]
pub fn generate_all(schemas: &[(String, ParsedSchema)], output_dir: &Path) {
    // 1. Generate capnp.ts runtime (shared across all services)
    let capnp_ts = message::generate_capnp_runtime();
    let capnp_path = output_dir.join("capnp.ts");
    std::fs::write(&capnp_path, capnp_ts).expect("Failed to write capnp.ts");
    println!("  Generated capnp.ts");

    // 2. Generate per-service files
    let mut service_names = Vec::new();
    for (name, schema) in schemas {
        let ts = generate_service_file(name, schema);
        let filename = format!("{name}.ts");
        let path = output_dir.join(&filename);
        std::fs::write(&path, ts).expect("Failed to write service file");
        println!("  Generated {filename}");
        service_names.push(name.clone());
    }

    // 3. Generate index.ts with re-exports
    let index_ts = generate_index(&service_names);
    let index_path = output_dir.join("index.ts");
    std::fs::write(&index_path, index_ts).expect("Failed to write index.ts");
    println!("  Generated index.ts");
}

/// Generate a single service TypeScript file.
fn generate_service_file(service_name: &str, schema: &ParsedSchema) -> String {
    let mut out = String::with_capacity(8192);

    // Header
    out.push_str(&format!(
        "// Generated by hyprstream-ts-codegen — DO NOT EDIT\n\
         // Service: {service_name}\n\n\
         import {{ CapnpMessageBuilder, CapnpReader }} from './capnp';\n"
    ));

    // Conditional streaming import
    if client::has_streaming_methods(schema) {
        out.push_str(
            "import { type StreamSubscription, createStreamSubscription } from '../stream-client';\n"
        );
    }
    out.push('\n');

    // Interfaces
    interfaces::generate_interfaces(&mut out, schema);

    // Builders (request serialization)
    builders::generate_builders(&mut out, service_name, schema);

    // Parsers (response deserialization)
    parsers::generate_parsers(&mut out, service_name, schema);

    // Client class
    client::generate_client(&mut out, service_name, schema);

    out
}

