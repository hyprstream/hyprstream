/// Generate all output files from parsed schemas using the given backend.
#[allow(clippy::print_stdout)]
pub fn generate_all<B: CodegenBackend>(
    backend: &B,
    schemas: &[(String, ParsedSchema)],
    output_dir: &Path,
) {
    // 1. Runtime file (e.g. capnp.ts)
    let (runtime_name, runtime_content) = backend.runtime_file();
    let runtime_path = output_dir.join(&runtime_name);
    std::fs::write(&runtime_path, runtime_content).expect("Failed to write runtime file");
    println!("  Generated {runtime_name}");

    // 2. Per-schema service files
    let mut all_names = Vec::new();
    let mut client_names = Vec::new();
    let mut module_contents = Vec::new();
    for (name, schema) in schemas {
        let content = backend.generate_service_file(name, schema);
        let filename = format!("{name}.{}", backend.file_extension());
        let path = output_dir.join(&filename);
        std::fs::write(&path, &content).expect("Failed to write service file");
        println!("  Generated {filename}");
        all_names.push(name.clone());
        if schema.request_struct.is_some() {
            client_names.push(name.clone());
        }
        module_contents.push((name.clone(), content));
    }

    // 3. Optional index/manifest file
    if let Some((index_name, index_content)) =
        backend.index_file(&all_names, &client_names, &module_contents)
    {
        let index_path = output_dir.join(&index_name);
        std::fs::write(&index_path, index_content).expect("Failed to write index file");
        println!("  Generated {index_name}");
    }
}

/// Generate a single TypeScript file for a schema.
///
/// For service schemas (with request/response structs): generates interfaces,
/// service builders/parsers, client class, plus standalone struct builders/parsers.
/// For data-only schemas: generates interfaces and standalone struct builders/parsers only.
fn generate_service_file(service_name: &str, schema: &ParsedSchema) -> String {
    let mut out = String::with_capacity(8192);

    let is_service = schema.request_struct.is_some();

    // Header
    out.push_str(&format!(
        "// Generated by hyprstream-ts-codegen — DO NOT EDIT\n\
         // {label}: {service_name}\n\n\
         import {{ CapnpArena, CapnpReader }} from './capnp';\n",
        label = if is_service { "Service" } else { "Schema" }
    ));

    // Inline StreamHandle interface (only for service schemas with streaming methods)
    if is_service && client::has_streaming_methods(schema) {
        out.push_str(
            "/** Verified stream handle (duck-type compatible with WASM StreamHandle). */\n\
             export interface StreamHandle {\n\
             \x20 nextPayload(): Promise<Uint8Array | null>;\n\
             \x20 cancel(): Promise<void>;\n\
             \x20 streamId(): string;\n\
             \x20 isCompleted(): boolean;\n\
             \x20 free(): void;\n\
             \x20 [Symbol.dispose](): void;\n\
             }\n\n",
        );
    }
    out.push('\n');

    // Interfaces
    interfaces::generate_interfaces(&mut out, schema);

    // Service builders/parsers (request/response union serialization)
    builders::generate_builders(&mut out, service_name, schema);
    parsers::generate_parsers(&mut out, service_name, schema);

    // Standalone struct builders/parsers (for all union-bearing structs in schema.structs)
    builders::generate_struct_builders(&mut out, schema);
    parsers::generate_struct_parsers(&mut out, schema);

    // Client class (only for service schemas)
    if is_service {
        client::generate_client(&mut out, service_name, schema);
    }

    out
}

